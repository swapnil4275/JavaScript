// Demonstrate Assignment Operators

// Simple Assignment Operator
let x1 = 20;
let y1 = 10 + x1;
console.log("= : ", y1);

// Addition ssignment Operator
let x2 = 30;
x2 += 20;
console.log("+= : ", x2);

// Subtraction Assignment Operator
let y2 = 50;
y2 -= 30;
console.log("-= : ", y2);

// Multiplication Assignment Operator
let x3 = 20;
x3 *= 10;
console.log("*= : ", x3);

// Division Assignment Operator
let x4 = 40;
x4 /= 4;
console.log("/= : ", x4);

// Exponentiation (Power) Assignment Operator
let x5 = 25;
x5 **= 2;
console.log("**= : ", x5);

// Remainder (Modulus) Assignment Operator
let x6 = 60;
x6 %= 7;
console.log("%= : ", x6);