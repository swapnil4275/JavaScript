// Demonstrate get method

let now = new Date();

console.log("date : ", now);

console.log("Year : ", now.getFullYear());  // print Year

console.log("Month : ", now.getMonth());  // Print Month

console.log("Day : ", now.getDate());  // print day

console.log("Week : ", now.getDay());  // print week

console.log("Hours : ", now.getHours());  // print Hours

console.log("Minutes : ", now.getMinutes());  // print Minutes

console.log("Seconds : ", now.getSeconds());  // print Seconds