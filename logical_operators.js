// Demonstrate Logical Operators 

// AND logical operator
let age = 18;
let ID = true;
console.log("Eligible for vote : ", age >= 18 && ID);


// OR logical operator
let paycash = true;
let payUPI = false;
console.log("payBill : ", paycash || payUPI);


// NOT logical operator
let phonelocked = true;
console.log("phone useable : ", !phonelocked);