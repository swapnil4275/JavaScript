// Demonstrate JS math 

console.log("math.round(4.6) : ", Math.round(4.6));  // math.round()

console.log("math.round(4.4) : ", Math.round(4.4));  // math.round()

console.log("math.ceil(5.1) : ", Math.ceil(5.1));  // math.ceil()

console.log("math.round(4.9) : ", Math.floor(4.9));  // math.floor()

console.log("math.trunc(6.6) : ", Math.trunc(6.6));  // math.trunc()

console.log("math.sign(4) : ", Math.sign(4));  // math.sign()

console.log("math.sign(-4) : ", Math.sign(-4));  // math.sign()

console.log("math.pow(8,2) : ", Math.pow(8, 2));  // math.pow()

console.log("math.sqrt() : ", Math.sqrt(64));  // math.sqrt()

console.log("math.abs() : ", Math.abs(-7.7));  // math.abs()


